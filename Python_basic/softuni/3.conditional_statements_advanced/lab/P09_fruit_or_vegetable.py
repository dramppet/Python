type_product = input()

if type_product == "banana" or type_product == "apple" or type_product == "kiwi" or type_product == "cherry" or type_product == "lemon" or type_product == "grapes":
    print("fruit")
elif type_product == "tomato" or type_product == "cucumber" or type_product == "pepper" or type_product == "carrot":
    print("vegetable")
else:
    print("unknown")